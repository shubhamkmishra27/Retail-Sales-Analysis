--12.Which product category has seen the max value of returns in the last 3 months of 
--transactions?
WITH max_date AS (
    SELECT MAX(tran_date) AS maxx
    FROM Transactions
)

SELECT 
    prod_cat_code,
    SUM(CASE 
            WHEN qty < 0 THEN qty 
            ELSE 0 
        END) AS total_return
FROM Transactions t
CROSS JOIN max_date m
WHERE t.tran_date BETWEEN DATEADD(MONTH, -3, m.maxx) AND m.maxx
GROUP BY prod_cat_code
ORDER BY TOTAL_RETURN ;


--13.Which store-type sells the maximum products; by value of sales amount and by quantity sold? 
   
SELECT TOP 1
    Store_type,
    SUM(CASE  WHEN total_amt > 0 THEN   total_amt ELSE 0 END) Sale ,
    SUM(CASE WHEN Qty > 0 THEN Qty ELSE 0 END) Sold_QTY
FROM Transactions
GROUP BY Store_type
ORDER BY SALE DESC, SOLD_QTY   DESC


--14.What are the categories for which average revenue is above the overall average. 
with avgg as (
    select 
        AVG(case when total_amt > 0 then total_amt else 0 end ) as average
    from Transactions 

)
select 
    prod_cat_code,
    avg(case when total_amt > 0 then total_amt else 0 end) cat_avg
from Transactions
cross join avgg 
group by prod_cat_code , average
having avg(case when total_amt > 0 then total_amt else 0 end) > average



--15. Find the average and total revenue by each subcategory for the categories which 
--are among top 5 categories in terms of quantity sold.
with top_5_cate  as (
        select top 5
                prod_cat_code,
                sum(case when Qty  >0 then Qty else 0 end ) as qty_sol
        from Transactions
        group by prod_cat_code
        order by qty_sol desc
)
select t.prod_cat_code,
t.prod_subcat_code,
        round(avg(case when total_amt > 0 then total_amt else 0 end ),2) as sales,
        round(sum(case when total_amt > 0 then total_amt else 0 end ),2) as sales

from Transactions t
left join top_5_cate c
on c. prod_cat_code  = t.prod_cat_code
where t.prod_cat_code in (select prod_cat_code from top_5_cate)
group by t.prod_cat_code,t.prod_subcat_code
order by  t.prod_cat_code asc
