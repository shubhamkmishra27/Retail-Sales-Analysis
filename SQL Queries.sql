--10.What is percentage of sales and returns by product sub category; display only top 5 sub categories in terms of sales?

with sales_data as(
	select prod_subcat_code,
	sum(case when Qty< 0 then total_amt  else 0 end  ) as return_sale,
	sum(case when qty > 0  then total_amt else 0 end) as sale
	from Transactions
	group by prod_subcat_code	

)	    
select top 5 
  prod_subcat_code,
    Sale,
    Return_Sale,
	ROUND(sale * 100 / SUM(SALE) OVER(),2) Sales_Percentage,
	ROUND(return_sale * 100 / SUM(return_sale) OVER(),2) Sales_Percentage
from sales_data
order by sale desc



