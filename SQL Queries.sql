SELECT prod_cat,
ROUND(SUM(total_amt),2) AS TOTAL_SALE
FROM Transactions T
LEFT JOIN prod_cat_info P
ON T.prod_cat_code = P.prod_cat_code AND
T.prod_subcat_code = P.prod_sub_cat_code
WHERE total_amt > 0 AND
prod_cat IN ('Electronics','Books')
GROUP BY prod_cat

